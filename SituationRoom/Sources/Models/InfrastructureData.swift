import Foundation
import CoreLocation

/// TeleGeography submarine cable
struct SubmarineCable: Identifiable {
    let id: String
    let name: String
    let color: String  // hex color from API
    let coordinates: [[CLLocationCoordinate2D]]  // MultiLineString segments
}

/// IODA internet outage alert
struct InternetOutage: Identifiable {
    let id: String
    let entityName: String
    let entityCode: String   // ISO country code
    let entityType: String   // "country", "asn", "region"
    let level: OutageLevel
    let datasource: String   // "bgp", "ping-slash24", "merit-nt"
    let coordinate: CLLocationCoordinate2D  // geocoded from country code

    enum OutageLevel: String {
        case critical = "critical"
        case warning = "warning"
        case normal = "normal"

        var displayColor: String {
            switch self {
            case .critical: return "red"
            case .warning: return "orange"
            case .normal: return "green"
            }
        }
    }
}

